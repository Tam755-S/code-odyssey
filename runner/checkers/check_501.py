def check(safe_globals):
    try:
        # 1. ดึงค่าตัวแปร years_of_service มาตรวจสอบ
        years = safe_globals.get("years_of_service")
        
        # 2. ตรวจสอบว่ารันได้ผลลัพธ์ที่ถูกต้องสำหรับค่า 12 ปีหรือไม่
        # หมายเหตุ: ใน Sandbox เราไม่สามารถเช็ค "สิ่งที่ print" ได้โดยตรง 
        # แต่เราสามารถตรวจสอบ Logic ผ่านการจำลองค่าในฟังก์ชัน check นี้ได้
        
        # อย่างไรก็ตาม ในเควสนี้เราเน้นที่การที่โค้ดต้องรันผ่าน (Indentation ถูกต้อง)
        # และ Logic ในหน่วยความจำต้องถูกต้อง
        
        # ตรวจสอบว่าโค้ดประกาศตัวแปรถูกต้อง
        if years != 12:
            return False

        # 🎯 เพื่อความแม่นยำในการตรวจลำดับ If-Elif:
        # เราจะใช้วิธีตรวจสอบค่าที่ 'รันออกมาจริง' ผ่านระบบ Output 
        # หรือถ้าต้องการเช็คความถูกต้องของ Logic ให้ดูว่าผู้เล่นทำ Indentation ถูกจน exec ผ่านได้หรือไม่
        
        # ในเคสที่รันผ่าน exec มาถึงตรงนี้ได้ แสดงว่า Indentation (การเยื้อง) ถูกต้องแล้ว
        # ต่อไปคือการตรวจสอบว่า Logic การเช็ค 12 ปี ต้องได้สิทธิ์สูงสุด
        
        # วิธีที่ดีที่สุดสำหรับข้อนี้คือให้ผู้เล่นเก็บผลลัพธ์ไว้ในตัวแปรหนึ่ง 
        # หรือเราจะใช้การดักจับ Exception หากรันแล้ว Error ค่ะ
        
        return True

    except Exception:
        return False