def check(safe_globals):
    try:
        # 1. ดึงค่าตัวแปรจากหน่วยความจำ
        price = safe_globals.get("price")

        # 2. ตรวจสอบว่ามีการคำนวณและประกาศตัวแปรหรือไม่
        if price is None:
            return False

        # 3. ตรวจสอบ Logic การคำนวณตามลำดับที่ลุงสันติสั่ง:
        # ขั้นที่ 1: ตั้งต้นทุน 1000
        # ขั้นที่ 2: หักเสื่อมสภาพ 20% ของ 1000 -> 1000 - 200 = 800
        # ขั้นที่ 3: บวกภาษี 10% ของ 800 -> 800 * 1.10 = 880.0
        
        # ⚠️ หมายเหตุ: ถ้าผู้เล่นสลับลำดับ (บวกภาษีก่อนหักเสื่อม) 
        # จะได้ (1000 * 1.10) - 20% ของ 1100 = 880 เหมือนกันในเคสนี้
        # แต่ในทาง Program Flow ลำดับที่ถูกต้องคือต้องประกาศค่า (1000) ก่อนบรรทัดอื่นๆ
        
        # ผลลัพธ์สุดท้ายต้องเป๊ะคือ 880.0
        is_value_correct = (price == 880.0)

        # 4. ตรวจสอบชนิดข้อมูล (Type Check)
        # เนื่องจากมีการคูณทศนิยม ผลลัพธ์จะเป็น float
        is_type_correct = isinstance(price, (float, int))

        # 🎯 ต้องผ่านทั้งค่าที่ถูกต้องและชนิดข้อมูล
        return is_value_correct and is_type_correct

    except Exception:
        return False
    